#include <stdio.h>
#include <time.h>
int getRandomNumber(int level);
void showQuestion(int level, int num1, int num2);
void success();
void fail();
int main(void)
{
	//문이 5개가 있고, 각 문마다 점점 어려운 수식 퀴즈가 출제
	//맞히면 통과, 틀리면 실패
	srand(time(NULL));
	int count = 0;//맞힌 문제 개수
	for (int i = 1; i <= 5; i++)
	{
		// x * y =?
		int num1 = getRandomNumber(i);
		int num2 = getRandomNumber(i);
		//printf("%d x %d ?\n", numm1, num2);
		showQuestion(i, num1, num2);

		int answer = -1;
		scanf_s("%d", &answer);
		if (answer == -1)
		{
			printf("프로그램을 종료합니다.\n");
			exit(0);//뒤에 뭐가 있든 바로 프로그램 종료
		}
		else if (answer == num1 * num2)
		{
			//성공
			success();
			count++;
		}
		else
		{
			//실패
			fail();
		}
	}
	printf("\n\n 당신은 5개의 비밀번호 중 %d 개를 맞혔습니다.\n", count);
	return 0;
}
int getRandomNumber(int level)
{
	return rand() % (level * 7) + 1;//랜덤한 수를 하나를 뽑아 (level*7)에서 +1을 해서 1부터 (level * 7) + 1까지 수를 말한다
}
