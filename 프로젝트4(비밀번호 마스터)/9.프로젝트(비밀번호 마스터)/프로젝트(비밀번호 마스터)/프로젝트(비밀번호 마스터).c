{
	printf("\n\n\n############ %d 번째 ############\n", level);
	printf("\n\t     %d x %d", num1, num2);//\t=스페이스바 10칸
	printf("\n\n################################\n");
	printf("\n 비밀번호를 입력하세요. (종료 : -1) >> ");

}
